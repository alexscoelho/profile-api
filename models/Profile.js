const { Schema, model } = require('mongoose');

const ProfileSchema = Schema({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  phone: {
    type: String,
    required: true,
  },
  providers: {
    type: Schema.Types.Array,
    ref: 'Provider',
  },
});

ProfileSchema.method('toJSON', function () {
  const { __v, _id, ...object } = this.toObject();
  object.id = _id;
  return object;
});

module.exports = model('Profile', ProfileSchema);
