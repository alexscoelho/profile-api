const { Schema, model } = require('mongoose');

const ProfileSchema = Schema({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  phone: {
    type: String,
    required: true,
  },
});

module.exports = model('Profile', ProfileSchema);
